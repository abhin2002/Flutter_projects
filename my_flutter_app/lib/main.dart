  void toggleFavorite([WordPair? pair]) {
    // final prefs = await SharedPreferences.getInstance();
    // await prefs.getString('favorites');
    pair = pair ?? current;
    if (favorites.contains(pair)) {
      favorites.remove(pair);
      
